package Entity;

public class RaceInfoEntity {

	private String raceId;
	private String raceDay;
	private String raceNumber;
	private String raceName;
	private String raceDetail;
	private String field;

	public String getRaceId() {
		return raceId;
	}

	public void setRaceId(String raceId) {
		this.raceId = raceId;
	}

	public String getRaceDay() {
		return raceDay;
	}

	public void setRaceDay(String raceDay) {
		this.raceDay = raceDay;
	}

	public String getRaceNumber() {
		return raceNumber;
	}

	public void setRaceNumber(String raceNumber) {
		this.raceNumber = raceNumber;
	}

	public String getRaceName() {
		return raceName;
	}

	public void setRaceName(String raceName) {
		this.raceName = raceName;
	}

	public String getRaceDetail() {
		return raceDetail;
	}

	public void setRaceDetail(String raceDetail) {
		this.raceDetail = raceDetail;
	}

	public String getFeild() {
		return field;
	}

	public void setFeild(String feild) {
		this.field = feild;
	}

}
